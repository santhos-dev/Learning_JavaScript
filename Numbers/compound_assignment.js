// Compound Assignment Operator
// Perform a mathematical operation and reassign to result to the variable in one line of code.

// Without compound assignment operator
let num = 5;
num = num + 2;

console.log(num);

// With Compound assignment operator

// In += addition assignment operator

let num2 = 10;
num2 += 2;

console.log(num2);

// ----------------------------------------

// In -= Subtraction assignment operator 

let num3 = 10;
num3 -= 5;

console.log(num3);

// ----------------------------------------

// In *= multiplication assignment operator

let num4 = 2;
num4 *= 3;

console.log(num4);

// ---------------------------------------

// In /= Division assignment operator

let num5 = 6;
num5 /= 2;

console.log(num5);

// -------------------------------------

// In % Modulus assignment operator

let num6 = 5;
num5 %= 3;

console.log(num5);

// -------------------------------------

// In ** exponent assignment operator

let num7 = 2;
num7 **= 2;

console.log(num7);

